/**
 * extrae una imagen que se encuetra encriptada en un string
 * @param AbsolutePath una imagen encriptada en un string de tipo AbsolutePath
 * @param label label para obtener el tamaño de este dimensionar la imagen y adaptarla
 * @return icono de la imamagen a insertar en un Label
 */
public ImageIcon insertarImagen(String AbsolutePath,JLabel label){ 
    ImageIcon icono=null;
  ImageIcon icon  = new ImageIcon(AbsolutePath); 
icono = new ImageIcon(icon.getImage().getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_DEFAULT)); 
return icono;
}


